


parciales = float(input("Ingrese la nota de los exámenes parciales (0-10): "))
practicos = float(input("Ingrese la nota de los trabajos prácticos (0-10): "))
integrador = float(input("Ingrese la nota del examen integrador (0-10): "))

nota_parciales = parciales * 0.3
nota_practicos = practicos * 0.2
nota_integrador = integrador * 0.5

nota_final = nota_parciales + nota_practicos + nota_integrador
print("La Nota final del estudiante es:", nota_final)

























