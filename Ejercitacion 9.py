


fecha = input("Ingrese la fecha en formato DDMMAAAA: ")

# Extraer el día, mes y año utilizando descomposición matemática
dia = int(fecha[:2])
mes = int(fecha[2:4])
año = int(fecha[4:])

# Imprimir el día, mes y año por separado
print("Día:", dia)
print("Mes:", mes)
print("Año:", año)
















